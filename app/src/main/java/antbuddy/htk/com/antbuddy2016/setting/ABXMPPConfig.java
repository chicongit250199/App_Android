public class ABXMPPConfig {
    public String HOST_XMPP = "";
    public String DOMAIN_XMPP = "";
    public int PORT_XMPP = 5222;

    public String USERNAME_XMPP = "";
    public String PASSWORD_XMPP = "";

    public String getHOST_XMPP() {
        return HOST_XMPP;
    }

    public void setHOST_XMPP(String HOST_XMPP) {
        this.HOST_XMPP = HOST_XMPP;
    }

    public String getDOMAIN_XMPP() {
        return DOMAIN_XMPP;
    }

    public void setDOMAIN_XMPP(String DOMAIN_XMPP) {
        this.DOMAIN_XMPP = DOMAIN_XMPP;
    }

    public int getPORT_XMPP() {
        return PORT_XMPP;
    }

    public void setPORT_XMPP(int PORT_XMPP) {
        this.PORT_XMPP = PORT_XMPP;
    }

    public String getUSERNAME_XMPP() {
        return USERNAME_XMPP;
    }

    public void setUSERNAME_XMPP(String USERNAME_XMPP) {
        this.USERNAME_XMPP = USERNAME_XMPP;
    }

    public String getPASSWORD_XMPP() {
        return PASSWORD_XMPP;
    }

    public void setPASSWORD_XMPP(String PASSWORD_XMPP) {
        this.PASSWORD_XMPP = PASSWORD_XMPP;
    }
}
